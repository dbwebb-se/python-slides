'''
Types
'''

# Assign variables
my_int = 42
my_float = 4.2
my_string = "Hello world!"

# Numbers
print(type(my_int)) # eller print(type(42))
print(type(my_float))

# String
print(type(my_string))
print(type("Hello world!"))

# Booleans
print(type(True))
print(type(False))

# None
# The None keyword is used to define a null value, or no value at all.
# None is not the same as 0, False, or an empty string. None is a data type of its own 
# (NoneType).
print(type(None))

# Multiline
hello = """
Hello
World!
"""
print(type(hello))
