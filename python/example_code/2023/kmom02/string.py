"""
"" vs '' in strings
"""

string1 = 'This is a string with single quotes.'
print(string1)

string2 = "This is a string with double quotes."
print(string2)

string3 = '"This is a string with double quotes within single quotes."'
print(string3)

string4 = "String with apostrophe, it's mine."
print(string4)

string5 = 'String with apostrophe, it\'s mine. (with backslash before the apostrophe)'
print(string5)

string6 = '"String with apostrophe, it\'s mine. (with backslash before the apostrophe)"'
print(string6)
